
# Operadores aritméticos
valorA = float(input("Digite o valor de A: "))
valorB = float(input("Digite o valor de B: "))

# Soma
soma = valorA + valorB
print(f"A soma = {soma}")

# Subtração
Subtracao = valorA - valorB
print(f"A subtração = {Subtracao}")

# Multiplicação
multiplicacao = valorA * valorB
print(f"A mutiplicação = {multiplicacao}")

# Divisão
Divisao = valorA / valorB
print(f"A Divisão = {Divisao}")

# Divisão inteira
Divisao_inteiro = valorA // valorB
print(f"A divisão inteira = {Divisao_inteiro}")

# Resto da Divisão
modulo = valorA % valorB
print(f"O resto da divisão = {modulo}")

# Exponenciação
exponenciacao = valorA ** valorB
print(f"A exponenciação = {exponenciacao}")
